     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $items = Office::where('active', 1)->orderBy('name','asc')->get(['id','name']);
        $records[] = [
            'name' => 'Все офисы',
            'code' => 0
        ];
        foreach ($items as $item) {
            $records[] = [
              'name' => $item->name,
              'code' => $item->id
            ];
        }
        return response()->json([
            "status" => true,
            "records" => $records
        ])->setStatusCode(200);
    }

